//! System font.

use super::types::{C8Addr, C8Byte};

/// System font struct.
pub struct Font(Vec<C8Byte>);

/// Font data address in memory.
pub const FONT_DATA_ADDR: C8Addr = 0;
/// Super font data address in memory.
pub const SUPER_FONT_DATA_ADDR: C8Addr = 0x50;
/// Font char height.
pub const FONT_CHAR_HEIGHT: usize = 5;
/// Super font char height.
pub const SUPER_FONT_CHAR_HEIGHT: usize = 10;

