impl Font {
    /// Create new system font.
    ///
    /// # Returns
    ///
    /// * Font instance.
    ///
    pub fn new_system_font() -> Self {
        Font(SYSTEM_FONT_DATA.to_vec())
    }

    /// Create new super system font.
    pub fn new_super_system_font() -> Self {
        Font(SUPER_SYSTEM_FONT_DATA.to_vec())
    }

    /// Get font data.
    pub fn get_data(&self) -> &[C8Byte] {
        &self.0
    }
}
